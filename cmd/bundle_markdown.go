func writeBundleKEDASection(w io.Writer, data *bundleData) {
	ki := data.StatusReport.Analysis.KEDAInfo
	if ki == nil {
		return // Omit entire section when KEDA is not detected.
	}

	_, _ = fmt.Fprint(w, "## KEDA Status\n\n")
	_, _ = fmt.Fprintf(w, "**ScaledObject:** %s\n\n", mdEscape(ki.ScaledObjectName))

	if ki.PollingInterval != nil {
		_, _ = fmt.Fprintf(w, "**Polling Interval:** %ds\n", *ki.PollingInterval)
	}
	if ki.CooldownPeriod != nil {
		_, _ = fmt.Fprintf(w, "**Cooldown Period:** %ds\n", *ki.CooldownPeriod)
	}
	if ki.MinReplicaCount != nil {
		_, _ = fmt.Fprintf(w, "**Min Replica Count:** %d\n", *ki.MinReplicaCount)
	}
	if ki.MaxReplicaCount != nil {
		_, _ = fmt.Fprintf(w, "**Max Replica Count:** %d\n", *ki.MaxReplicaCount)
	}

	if len(ki.Triggers) > 0 {
		_, _ = fmt.Fprint(w, "\n### Triggers\n\n")
		_, _ = fmt.Fprint(w, "| Type | Name | Status | Threshold | Current | Auth Ref |\n")
		_, _ = fmt.Fprint(w, "|------|------|--------|-----------|---------|----------|\n")
		for _, t := range ki.Triggers {
			authRef := "-"
			if t.AuthRef != "" {
				authRef = mdEscape(t.AuthRef)
			}
			_, _ = fmt.Fprintf(w, "| %s | %s | %s | %s | %s | %s |\n",
				mdEscape(t.Type), mdEscape(t.Name), mdEscape(t.Status),
				mdEscape(t.Threshold), mdEscape(t.CurrentValue), authRef)
		}
		_, _ = fmt.Fprintln(w)
	}

	if ki.Fallback != nil {
		_, _ = fmt.Fprintf(w, "**Fallback:** failureThreshold=%d, replicas=%d\n\n",
			ki.Fallback.FailureThreshold, ki.Fallback.Replicas)
	}

	if len(ki.Lines) > 0 {
		_, _ = fmt.Fprint(w, "**Notes:**\n")
		for _, line := range ki.Lines {
			_, _ = fmt.Fprintf(w, "- %s\n", mdEscape(line))
		}
		_, _ = fmt.Fprintln(w)
	}

	_, _ = fmt.Fprint(w, "\n---\n\n")
}

