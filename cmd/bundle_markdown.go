func writeBundlePodStatusTable(w io.Writer, data *bundleData) {
	_, _ = fmt.Fprint(w, "## Pod Status\n\n")
	if len(data.PodInfos) == 0 {
		_, _ = fmt.Fprint(w, "_No pod information available._\n\n---\n\n")
		return
	}

	_, _ = fmt.Fprint(w, "| Name | Phase | Ready | Unschedulable | Reasons | Node |\n")
	_, _ = fmt.Fprint(w, "|------|-------|-------|---------------|---------|------|\n")
	for _, pod := range data.PodInfos {
		ready := "✓"
		if !pod.Ready {
			ready = "✗"
		}
		unschedulable := "-"
		if pod.Unschedulable {
			unschedulable = "Yes"
		}
		reasons := "-"
		if len(pod.Reasons) > 0 {
			reasons = mdEscape(strings.Join(pod.Reasons, ", "))
		}
		node := pod.NodeName
		if node == "" {
			node = "-"
		}
		_, _ = fmt.Fprintf(w, "| %s | %s | %s | %s | %s | %s |\n",
			mdEscape(pod.Name), mdEscape(pod.Phase), ready, unschedulable, reasons, mdEscape(node))
	}
	_, _ = fmt.Fprint(w, "\n---\n\n")
}

