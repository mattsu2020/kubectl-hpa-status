// writeBundleMarkdown renders all bundle sections to an io.Writer.
// Errors from write operations are not propagated; this is best-effort
// rendering consistent with the snapshot command's pattern.
func writeBundleMarkdown(w io.Writer, data *bundleData) {
	_, _ = fmt.Fprintf(w, "# HPA Investigation Bundle: %s/%s\n\n", data.Namespace, data.HPAName)
	_, _ = fmt.Fprintf(w, "**Generated:** %s  \n", data.Timestamp.Format(time.RFC3339))
	_, _ = fmt.Fprintf(w, "**Plugin:** kubectl-hpa-status bundle\n\n")
	_, _ = fmt.Fprint(w, "---\n\n")

	writeBundleTOC(w)
	_, _ = fmt.Fprint(w, "---\n\n")

	writeBundleStatusSummary(w, data)
	writeBundleHPAYAML(w, data)
	writeBundleScaleTarget(w, data)
	writeBundlePodStatusTable(w, data)
	writeBundleContainerStatusTable(w, data)
	writeBundleResourceRequests(w, data)
	writeBundleEventsSection(w, data)
	writeBundleMetricsAPI(w, data)
	writeBundleMetricsDiagnostics(w, data)
	writeBundleCapacityContextSection(w, data)
	writeBundleScalePathSection(w, data)
	writeBundleBlockerSection(w, data)
	writeBundleKEDASection(w, data)
	writeBundleQuotasSection(w, data)
	writeBundleLimitRangesSection(w, data)
	writeBundlePDBsSection(w, data)
	writeBundleNodeCapacitySection(w, data)
	writeBundleRecommendations(w, data)
	writeBundleFullAnalysis(w, data)

	_, _ = fmt.Fprint(w, "---\n_Generated by kubectl-hpa-status bundle_\n")
}

func writeBundleTOC(w io.Writer) {
	_, _ = fmt.Fprint(w, "## Table of Contents\n\n")
	sections := []string{
		"HPA Status Summary",
		"HPA Resource (YAML)",
		"Scale Target Resource",
		"Pod Status",
		"Container Status",
		"Resource Requests/Limits",
		"Events",
		"Metrics API Status",
		"Metrics Diagnostics",
		"Capacity Context",
		"Scale Path",
		"Blocker Analysis",
		"KEDA Status",
		"ResourceQuotas",
		"LimitRanges",
		"PodDisruptionBudgets",
		"Node Capacity",
		"Recommendations",
		"Full Analysis Report",
	}
	for i, s := range sections {
		anchor := strings.ToLower(strings.ReplaceAll(s, " ", "-"))
		anchor = strings.ReplaceAll(anchor, "(", "")
		anchor = strings.ReplaceAll(anchor, ")", "")
		anchor = strings.ReplaceAll(anchor, "/", "")
		_, _ = fmt.Fprintf(w, "%d. [%s](#%s)\n", i+1, s, anchor)
	}
	_, _ = fmt.Fprintln(w)
}

func writeBundleStatusSummary(w io.Writer, data *bundleData) {
	_, _ = fmt.Fprint(w, "## HPA Status Summary\n\n")
	if err := hpaanalysis.WriteMarkdownReport(w, data.StatusReport); err != nil {
		_, _ = fmt.Fprintf(w, "_Error rendering status summary: %v_\n", err)
	}
	_, _ = fmt.Fprint(w, "\n---\n\n")
}

