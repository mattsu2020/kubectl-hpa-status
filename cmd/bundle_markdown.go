// bundleWriter wraps an io.Writer and captures the first write error so that
// the bundle rendering functions can keep emitting subsequent sections (to
// preserve ordering/content) while still surfacing a failure to the caller.
//
// Write errors after the first are intentionally ignored: we only need to know
// whether the output succeeded at all, and continuing lets a partially-written
// stream (e.g. a real file that ran out of space) still contain as much of the
// intended report as possible.
type bundleWriter struct {
	w   io.Writer
	err error
}

// Printf writes a formatted record. The error from the underlying writer is
// captured the first time it occurs and never overwrites a previously captured
// error.
func (b *bundleWriter) Printf(format string, args ...any) {
	if b.err != nil {
		return
	}
	_, b.err = fmt.Fprintf(b.w, format, args...)
}

// Print writes its operands with no format directive.
func (b *bundleWriter) Print(args ...any) {
	if b.err != nil {
		return
	}
	_, b.err = fmt.Fprint(b.w, args...)
}

// Println writes its operands followed by a newline.
func (b *bundleWriter) Println(args ...any) {
	if b.err != nil {
		return
	}
	_, b.err = fmt.Fprintln(b.w, args...)
}

// Write writes raw bytes, capturing the first error.
func (b *bundleWriter) Write(p []byte) {
	if b.err != nil {
		return
	}
	_, b.err = b.w.Write(p)
}

// writeBundleMarkdown renders all bundle sections to an io.Writer. The first
// write error encountered is returned; rendering otherwise proceeds in order so
// that a partial sink still receives as much of the report as possible.
func writeBundleMarkdown(w io.Writer, data *bundleData) error {
	b := &bundleWriter{w: w}
	b.Printf("# HPA Investigation Bundle: %s/%s\n\n", data.Namespace, data.HPAName)
	b.Printf("**Generated:** %s  \n", data.Timestamp.Format(time.RFC3339))
	b.Printf("**Plugin:** kubectl-hpa-status bundle\n\n")
	b.Print("---\n\n")

	writeBundleTOC(b)
	b.Print("---\n\n")

	writeBundleStatusSummary(b, data)
	writeBundleHPAYAML(b, data)
	writeBundleScaleTarget(b, data)
	writeBundlePodStatusTable(b, data)
	writeBundleContainerStatusTable(b, data)
	writeBundleResourceRequests(b, data)
	writeBundleEventsSection(b, data)
	writeBundleMetricsAPI(b, data)
	writeBundleMetricsDiagnostics(b, data)
	writeBundleCapacityContextSection(b, data)
	writeBundleScalePathSection(b, data)
	writeBundleBlockerSection(b, data)
	writeBundleKEDASection(b, data)
	writeBundleQuotasSection(b, data)
	writeBundleLimitRangesSection(b, data)
	writeBundlePDBsSection(b, data)
	writeBundleNodeCapacitySection(b, data)
	writeBundleRecommendations(b, data)
	writeBundleFullAnalysis(b, data)
	writeBundleWarnings(b, data)

	b.Print("---\n_Generated by kubectl-hpa-status bundle_\n")
	return b.err
}

func writeBundleTOC(b *bundleWriter) {
	b.Print("## Table of Contents\n\n")
	sections := []string{
		"HPA Status Summary",
		"HPA Resource (YAML)",
		"Scale Target Resource",
		"Pod Status",
		"Container Status",
		"Resource Requests/Limits",
		"Events",
		"Metrics API Status",
		"Metrics Diagnostics",
		"Capacity Context",
		"Scale Path",
		"Blocker Analysis",
		"KEDA Status",
		"ResourceQuotas",
		"LimitRanges",
		"PodDisruptionBudgets",
		"Node Capacity",
		"Recommendations",
		"Full Analysis Report",
	}
	for i, s := range sections {
		anchor := strings.ToLower(strings.ReplaceAll(s, " ", "-"))
		anchor = strings.ReplaceAll(anchor, "(", "")
		anchor = strings.ReplaceAll(anchor, ")", "")
		anchor = strings.ReplaceAll(anchor, "/", "")
		b.Printf("%d. [%s](#%s)\n", i+1, s, anchor)
	}
	b.Println()
}

func writeBundleStatusSummary(b *bundleWriter, data *bundleData) {
	b.Print("## HPA Status Summary\n\n")
	if err := hpaanalysis.WriteMarkdownReport(b.w, data.StatusReport); err != nil {
		b.Printf("_Error rendering status summary: %v_\n", err)
	}
	b.Print("\n---\n\n")
}

