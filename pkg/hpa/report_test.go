func TestWriteMarkdownReportEscapesPipes(t *testing.T) {
	report := StatusReport{
		Analysis: Analysis{
			Namespace: "default",
			Name:      "pipe-test",
			Target:    "Deployment/svc|web",
			Conditions: []Condition{
				{Type: "ScalingActive", Status: "True", Reason: "OK", Message: "metric | pipe"},
			},
		},
	}
	var buf bytes.Buffer
	if err := WriteMarkdownReport(&buf, report); err != nil {
		t.Fatalf("WriteMarkdownReport returned error: %v", err)
	}
	output := buf.String()

	if strings.Contains(output, "metric | pipe") {
		t.Errorf("expected pipe to be escaped in conditions table, got:\n%s", output)
	}
	if !strings.Contains(output, `metric \| pipe`) {
		t.Errorf("expected escaped pipe in conditions table, got:\n%s", output)
	}
}

func TestWriteHTMLReportHasStructure(t *testing.T) {
	report := sampleStatusReport()
	var buf bytes.Buffer
	if err := WriteHTMLReport(&buf, report); err != nil {
		t.Fatalf("WriteHTMLReport returned error: %v", err)
	}
	output := buf.String()

	if !strings.HasPrefix(output, "<!DOCTYPE html>") {
		t.Errorf("expected HTML to start with DOCTYPE, got:\n%s", output[:50])
	}
	if !strings.Contains(output, "</html>") {
		t.Errorf("expected closing </html> tag, got:\n%s", output)
	}
	if !strings.Contains(output, "<head>") {
		t.Errorf("expected <head> tag, got:\n%s", output)
	}
	if !strings.Contains(output, "</head>") {
		t.Errorf("expected </head> tag, got:\n%s", output)
	}
	if !strings.Contains(output, "<body>") {
		t.Errorf("expected <body> tag, got:\n%s", output)
	}
	if !strings.Contains(output, "</body>") {
		t.Errorf("expected </body> tag, got:\n%s", output)
	}
	if !strings.Contains(output, "<style>") {
		t.Errorf("expected <style> tag for inline CSS, got:\n%s", output)
	}
}

