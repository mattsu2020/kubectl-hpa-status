func TestHTMLEscape(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"hello", "hello"},
		{"<script>", "&lt;script&gt;"},
		{"a & b", "a &amp; b"},
		{`"quoted"`, "&quot;quoted&quot;"},
	}

	for _, tc := range tests {
		t.Run(tc.input, func(t *testing.T) {
			got := htmlEscape(tc.input)
			if got != tc.expected {
				t.Errorf("htmlEscape(%q) = %q, want %q", tc.input, got, tc.expected)
			}
		})
	}
}

