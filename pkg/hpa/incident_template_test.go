func TestWriteRichIncidentMarkdown_SingleHealthyHPA(t *testing.T) {
	reports := []StatusReport{
		{
			Analysis: Analysis{
				Namespace:   "default",
				Name:        "web-hpa",
				Target:      "Deployment/web",
				Current:     5,
				Desired:     5,
				Min:         2,
				Max:         10,
				Health:      "OK",
				HealthScore: 95,
				Summary:     "HPA is operating normally.",
				Conditions: []Condition{
					{Type: "ScalingActive", Status: "True", Reason: "ValidMetricFound", Message: "the HPA was able to successfully calculate a replica count"},
				},
				Metrics: []Metric{
					{Name: "cpu", Current: "78%", Target: "80%", Ratio: floatPtr(0.975), Text: "cpu 78%/80%"},
				},
			},
		},
	}

	var buf bytes.Buffer
	if err := WriteRichIncidentMarkdown(&buf, reports); err != nil {
		t.Fatalf("WriteRichIncidentMarkdown returned error: %v", err)
	}
	output := buf.String()

	expectedSections := []string{
		"# HPA Incident Report:",
		"## Incident Summary",
		"## Executive Summary",
		"## Affected Workloads",
		"## Remediation Steps",
		"## Recommendations",
		"## Escalation Notes",
		"Generated by kubectl-hpa-status",
	}
	for _, section := range expectedSections {
		if !strings.Contains(output, section) {
			t.Errorf("expected output to contain %q, got:\n%s", section, output)
		}
	}

	if !strings.Contains(output, "web-hpa") {
		t.Error("expected output to contain HPA name web-hpa")
	}
	if !strings.Contains(output, "OK") {
		t.Error("expected output to contain health state OK")
	}
	if !strings.Contains(output, "SEVERITY: LOW") {
		t.Error("expected severity LOW for healthy HPA")
	}
}

