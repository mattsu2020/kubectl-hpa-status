func TestWriteRichIncidentMarkdown_ValidMarkdown(t *testing.T) {
	reports := []StatusReport{
		{
			Analysis: Analysis{
				Namespace:   "default",
				Name:        "web-hpa",
				Target:      "Deployment/web",
				Current:     5,
				Desired:     5,
				Min:         2,
				Max:         10,
				Health:      "OK",
				HealthScore: 95,
				Summary:     "Operating normally.",
				Conditions: []Condition{
					{Type: "ScalingActive", Status: "True", Reason: "ValidMetricFound", Message: "the HPA was able to successfully calculate a replica count"},
				},
				Metrics: []Metric{
					{Name: "cpu", Current: "78%", Target: "80%", Ratio: floatPtr(0.975), Text: "cpu 78%/80%"},
				},
			},
			Events: []Event{
				{Reason: "SuccessfulRescale", Message: "New size: 5", Timestamp: time.Now()},
			},
		},
	}

	var buf bytes.Buffer
	if err := WriteRichIncidentMarkdown(&buf, reports); err != nil {
		t.Fatalf("WriteRichIncidentMarkdown returned error: %v", err)
	}
	output := buf.String()

	if strings.Contains(output, "||") {
		t.Error("output should not contain unescaped double pipes")
	}
	lines := strings.Split(output, "\n")
	for i, line := range lines {
		if strings.HasPrefix(line, "|") && strings.HasSuffix(line, "|") {
			parts := strings.Split(line, "|")
			if len(parts) > 2 {
				for _, cell := range parts[1 : len(parts)-1] {
					if strings.Contains(cell, "|") && !strings.Contains(cell, "\\|") {
						t.Errorf("line %d has unescaped pipe in table cell: %q", i, line)
					}
				}
			}
		}
	}
}

