func TestWriteRichIncidentMarkdown_SeverityAssessment(t *testing.T) {
	tests := []struct {
		health   string
		score    int
		expected string
	}{
		{"OK", 100, "LOW"},
		{"STABILIZED", 90, "LOW"},
		{"LIMITED", 75, "MEDIUM"},
		{"ERROR", 50, "HIGH"},
		{"ERROR", 20, "CRITICAL"},
	}

	for _, tc := range tests {
		t.Run(tc.health+"_"+string(rune(tc.score+'0')), func(t *testing.T) {
			reports := []StatusReport{
				{
					Analysis: Analysis{
						Name:        "test",
						Namespace:   "default",
						Target:      "Deployment/test",
						Health:      tc.health,
						HealthScore: tc.score,
					},
				},
			}
			var buf bytes.Buffer
			if err := WriteRichIncidentMarkdown(&buf, reports); err != nil {
				t.Fatalf("WriteRichIncidentMarkdown returned error: %v", err)
			}
			if !strings.Contains(buf.String(), "SEVERITY: "+tc.expected) {
				t.Errorf("expected SEVERITY: %s for health=%s score=%d, got:\n%s", tc.expected, tc.health, tc.score, buf.String())
			}
		})
	}
}

